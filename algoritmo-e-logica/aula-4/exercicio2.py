# Fazer um programa que leia uma temperatura fornecida em graus Fahrenheit 
# e a converta para o seu equivalente em graus Celsius, 
# imprimindo este valor na tela.
# Dado: C = 5/9*(F-32)

grausF = float(input("Escreta a temperatura em graus Fahrenheit: "))

grausC = 5/9*(grausF-32)

print(grausF, "graus Fahrenheit equivale a", grausC, "graus Celsius.")

